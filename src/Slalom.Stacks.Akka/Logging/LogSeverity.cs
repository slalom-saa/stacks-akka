namespace Slalom.Stacks.Messaging.Logging
{
    /// <summary>
    /// Indicates the log severity.
    /// </summary>
    public enum LogSeverity
    {
        /// <summary>
        /// Indicates a debug severity.
        /// </summary>
        Debug,

        /// <summary>
        /// Indicates a error severity.
        /// </summary>
        Error,

        /// <summary>
        /// Indicates a fatal severity.
        /// </summary>
        Fatal,

        /// <summary>
        /// Indicates a information severity.
        /// </summary>
        Information,

        /// <summary>
        /// Indicates a verbose severity.
        /// </summary>
        Verbose,

        /// <summary>
        /// Indicates a warning severity.
        /// </summary>
        Warning
    }
}